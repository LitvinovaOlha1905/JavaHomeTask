package de.telran.lesson4.homework2;

public class Calculator {
        double x;
        double y;

        //Метод для сложения
        public double add() {
            double result = x + y;
            return result;
        }
        //Метод для вычитания
        public double sub() {
            double result = x - y;
            return result;
        }
        //Метод для сложения
        public double mult(){
            double result = x * y;
            return result;
        }
        //Метод для деления
        public double div(){
            double result = x / y;
            return result;
        }
}

